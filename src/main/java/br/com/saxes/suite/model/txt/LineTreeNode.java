package br.com.saxes.suite.model.txt;

import br.com.saxes.suite.model.TreeNode;

public class LineTreeNode extends TreeNode {

    @Override
    public Object clone() throws CloneNotSupportedException {
        LineTreeNode clone = new LineTreeNode();

        clone( clone );

        return clone;
    }

    protected void clone( LineTreeNode line ) throws CloneNotSupportedException {
        super.clone( line );
    }

}
